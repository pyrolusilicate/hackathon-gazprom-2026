"""
Центральные константы пайплайна.

Здесь собраны все «крутилки», которые имеет смысл менять целиком:
  * DPI рендеринга для YOLO- и PDF-слоёв
  * Стандартные директории (raw / output / weights)
  * Размеры изображений для OCR и итогового PNG
  * Пороги матчинга Docling-item ↔ YOLO-block

Модули пайплайна импортируют из этого файла, чтобы не дублировать магические
числа и не рассинхронизироваться между собой.
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# DPI
# ---------------------------------------------------------------------------

# YOLO-растр: 400 DPI — высокая чёткость мелкого текста и аккуратные bbox'ы.
LAYOUT_DPI = 400

# Нативный PDF DPI.
PDF_DPI = 72

# Производные коэффициенты. Используются в coord_projection и layout_router.
PDF_TO_LAYOUT = PDF_DPI / LAYOUT_DPI   # points * PDF_TO_LAYOUT → pixels (inv)
LAYOUT_TO_PDF = LAYOUT_DPI / PDF_DPI   # points * LAYOUT_TO_PDF → pixels (fwd)


# ---------------------------------------------------------------------------
# Директории
# ---------------------------------------------------------------------------

OUTPUT_DIR = "data/output"
RAW_DIR = "data/raw"
WEIGHTS_DIR = "weights"


# ---------------------------------------------------------------------------
# Размеры изображений
# ---------------------------------------------------------------------------

# Длинная сторона кропа для olmOCR — жёстко требуется моделью.
OLM_RENDER_SIDE = 1288

# Финальное сжатие PNG в submission: метрика картинок мало весит,
# а большие PNG раздувают zip. 150px — достаточно для визуальной идентификации.
IMAGE_MAX_SIDE = 150


# ---------------------------------------------------------------------------
# Пороги матчинга Docling-item ↔ YOLO-block
# ---------------------------------------------------------------------------

# Минимальный IoM (intersection over min-area) для признания «тот же блок».
IOM_MATCH_THRESHOLD = 0.6

# Docling-item не должен быть > MAX_AREA_RATIO × YOLO-блока.
# Защита от ситуации «один огромный Docling-item на всю страницу» склеивает
# весь текст и через _used_item_ids блокирует остальные YOLO-блоки.
MAX_AREA_RATIO = 8.0
